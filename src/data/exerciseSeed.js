import { buildImageId, slugify } from '../lib/customWorkouts.js'

function makeExercise(muscleGroup, defaults, entry) {
  const config = typeof entry === 'string' ? { name: entry } : entry

  return {
    name: config.name,
    slug: slugify(config.name),
    muscle_group: muscleGroup,
    secondary_muscles: config.secondary_muscles ?? defaults.secondary_muscles ?? [],
    equipment: config.equipment ?? defaults.equipment ?? 'bodyweight',
    movement_type: config.movement_type ?? defaults.movement_type ?? 'isolation',
    force: config.force ?? defaults.force ?? null,
    mechanic: config.mechanic ?? defaults.mechanic ?? null,
    instructions: config.instructions ?? defaults.instructions ?? 'Move under control and own the end range.',
    image_id: config.image_id ?? buildImageId(config.name),
  }
}

function buildGroup(muscleGroup, defaults, entries) {
  return entries.map((entry) => makeExercise(muscleGroup, defaults, entry))
}

